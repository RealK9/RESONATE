# WavCaps Downloader (403k pairs)
# ---------------------------------------------------------------------------

class WavCapsDownloader:
    """
    Downloads WavCaps dataset — 403k audio clips with natural language captions.
    Captions generated by ChatGPT from existing audio metadata, then filtered.
    """

    def __init__(self, data_root: Path = DEFAULT_DATA_ROOT):
        self.data_dir = data_root / "wavcaps"
        self.data_dir.mkdir(parents=True, exist_ok=True)
        self._session = requests.Session()

    def download_metadata(self) -> dict[str, list[dict]]:
        """Download all WavCaps metadata JSON files."""
        all_metadata = {}

        for source_name, url in WAVCAPS_URLS.items():
            json_path = self.data_dir / f"{source_name}.json"

            if json_path.exists():
                logger.info(f"  WavCaps {source_name}: already downloaded")
                with open(json_path) as f:
                    all_metadata[source_name] = json.load(f)["data"]
                continue

            logger.info(f"  Downloading WavCaps {source_name} metadata...")
            resp = self._session.get(url, timeout=60)
            resp.raise_for_status()

            with open(json_path, "wb") as f:
                f.write(resp.content)

            data = resp.json()
            all_metadata[source_name] = data.get("data", data)
            logger.info(f"    {len(all_metadata[source_name]):,} entries")

        return all_metadata

    def download_audio(self, metadata: dict[str, list[dict]], max_per_source: int = 0) -> list[TextAudioPair]:
        """Download audio files from WavCaps metadata."""
        pairs = []
        audio_dir = self.data_dir / "audio"
        audio_dir.mkdir(exist_ok=True)

        for source_name, entries in metadata.items():
            source_dir = audio_dir / source_name
            source_dir.mkdir(exist_ok=True)

            items = entries[:max_per_source] if max_per_source > 0 else entries
            downloaded = 0

            for entry in tqdm(items, desc=f"WavCaps/{source_name}", unit="clip"):
                # Each entry has: id, caption, audio (URL or path), duration
                caption = entry.get("caption", "")
                audio_url = entry.get("download_link") or entry.get("audio", "")
                entry_id = entry.get("id", str(hash(caption)))[:32]
                duration = entry.get("duration", 0)

                if not audio_url or not caption:
                    continue

                # Determine file extension
                ext = ".flac" if ".flac" in audio_url else ".wav" if ".wav" in audio_url else ".mp3"
                audio_path = source_dir / f"{entry_id}{ext}"

                if not audio_path.exists():
                    try:
                        resp = self._session.get(audio_url, timeout=30)
                        if resp.status_code == 200:
                            audio_path.write_bytes(resp.content)
                            downloaded += 1
                        time.sleep(0.05)  # be nice to servers
                    except Exception as e:
                        logger.debug(f"Failed to download {entry_id}: {e}")
                        continue

                if audio_path.exists():
                    pairs.append(TextAudioPair(
                        audio_path=str(audio_path),
                        caption=caption,
                        source=f"wavcaps_{source_name}",
                        audio_url=audio_url,
                        duration=duration,
                    ))

            logger.info(f"  WavCaps/{source_name}: {len(pairs):,} pairs ({downloaded} newly downloaded)")

        return pairs


