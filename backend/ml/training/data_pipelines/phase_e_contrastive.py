class ProductionCaptionGenerator:
    """
    Generates text-audio pairs from RESONATE's own knowledge corpus.
    Uses our genre taxonomy, instrument encyclopedia, production techniques,
    and music theory knowledge to create rich, domain-specific captions.
    """

    def __init__(self):
        self._genre_taxonomy = None
        self._instruments = None
        self._techniques = None
        self._theory = None

    def _load_knowledge(self):
        """Load RESONATE knowledge modules."""
        try:
            from ml.training.knowledge.genre_taxonomy import GENRE_TAXONOMY
            self._genre_taxonomy = GENRE_TAXONOMY
        except ImportError:
            self._genre_taxonomy = {}

        try:
            from ml.training.knowledge.instruments import INSTRUMENT_FAMILIES
            self._instruments = INSTRUMENT_FAMILIES
        except ImportError:
            self._instruments = {}

        try:
            from ml.training.knowledge.production_techniques import PRODUCTION_TECHNIQUES
            self._techniques = PRODUCTION_TECHNIQUES
        except ImportError:
            self._techniques = {}

        try:
            from ml.training.knowledge.music_theory import MUSIC_THEORY
            self._theory = MUSIC_THEORY
        except ImportError:
            self._theory = {}

    def generate_captions_for_samples(self, samples: list) -> list[TextAudioPair]:
        """Generate text captions for existing audio samples using knowledge."""
        if not self._genre_taxonomy:
            self._load_knowledge()

        pairs = []
        for sample in samples:
            caption_parts = []

            # Role-based caption
            role = getattr(sample, "role", None)
            if role is not None:
                role_names = {
                    0: "kick drum", 1: "snare drum", 2: "clap",
                    3: "hi-hat", 4: "percussion", 5: "bass",
                    6: "lead synth", 7: "pad", 8: "sound effect",
                    9: "texture", 10: "vocal",
                }
                role_name = role_names.get(role, "unknown")
                caption_parts.append(f"A {role_name} sample")

            # Genre context
            genre = getattr(sample, "genre", None)
            if genre:
                caption_parts.append(f"in the style of {genre}")

            # BPM
            bpm = getattr(sample, "bpm", None)
            if bpm and bpm > 0:
                caption_parts.append(f"at {bpm:.0f} BPM")

            # Key
            key = getattr(sample, "key", None)
            if key:
                caption_parts.append(f"in the key of {key}")

            if caption_parts:
                caption = " ".join(caption_parts)
                pairs.append(TextAudioPair(
                    audio_path=getattr(sample, "filepath", ""),
                    caption=caption,
                    source="resonate_knowledge",
                    tags=[],
                ))

        logger.info(f"  Generated {len(pairs):,} knowledge-enriched captions")
        return pairs


