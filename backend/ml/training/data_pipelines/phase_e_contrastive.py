class AudioSetDownloader:
    """
    Downloads AudioSet metadata and ontology.
    Audio must be fetched from YouTube (yt-dlp required).
    527 sound event classes, 2M+ 10-second clips.
    """

    def __init__(self, data_root: Path = DEFAULT_DATA_ROOT):
        self.data_dir = data_root / "audioset"
        self.data_dir.mkdir(parents=True, exist_ok=True)
        self._ontology: dict[str, str] = {}

    def download_ontology(self) -> dict[str, str]:
        """Download AudioSet ontology (label ID → human-readable name)."""
        ontology_path = self.data_dir / "ontology.json"

        if not ontology_path.exists():
            logger.info("  Downloading AudioSet ontology...")
            resp = requests.get(AUDIOSET_ONTOLOGY_URL, timeout=30)
            resp.raise_for_status()
            ontology_path.write_bytes(resp.content)

        with open(ontology_path) as f:
            ontology = json.load(f)

        self._ontology = {item["id"]: item["name"] for item in ontology}
        logger.info(f"  AudioSet ontology: {len(self._ontology)} classes")
        return self._ontology

    def download_segment_csvs(self) -> dict[str, list[dict]]:
        """Download AudioSet segment CSV files (balanced train, eval, unbalanced)."""
        all_segments = {}

        for split_name, url in AUDIOSET_CSV_URLS.items():
            csv_path = self.data_dir / f"{split_name}_segments.csv"

            if not csv_path.exists():
                logger.info(f"  Downloading AudioSet {split_name} segments...")
                resp = requests.get(url, timeout=120)
                resp.raise_for_status()
                csv_path.write_bytes(resp.content)

            # Parse CSV (first 3 lines are comments)
            segments = []
            with open(csv_path) as f:
                for line in f:
                    if line.startswith("#") or not line.strip():
                        continue
                    parts = line.strip().split(", ")
                    if len(parts) >= 4:
                        ytid = parts[0].strip()
                        start = float(parts[1].strip())
                        end = float(parts[2].strip())
                        label_ids = [lid.strip().strip('"') for lid in parts[3:]]
                        segments.append({
                            "ytid": ytid,
                            "start": start,
                            "end": end,
                            "label_ids": label_ids,
                        })

            all_segments[split_name] = segments
            logger.info(f"  AudioSet {split_name}: {len(segments):,} segments")

        return all_segments

    def segments_to_pairs(self, segments: dict[str, list[dict]]) -> list[TextAudioPair]:
        """Convert AudioSet segments to text-audio pairs using ontology labels."""
        if not self._ontology:
            self.download_ontology()

        pairs = []
        for split_name, split_segments in segments.items():
            for seg in split_segments:
                label_names = [self._ontology.get(lid, lid) for lid in seg["label_ids"]]
                # Create natural language caption from labels
                if len(label_names) == 1:
                    caption = f"A sound of {label_names[0].lower()}"
                elif len(label_names) == 2:
                    caption = f"A sound containing {label_names[0].lower()} and {label_names[1].lower()}"
                else:
                    caption = f"A sound containing {', '.join(l.lower() for l in label_names[:-1])}, and {label_names[-1].lower()}"

                pairs.append(TextAudioPair(
                    audio_path="",  # Downloaded on-demand via yt-dlp
                    caption=caption,
                    source=f"audioset_{split_name}",
                    audio_url=f"https://www.youtube.com/watch?v={seg['ytid']}",
                    duration=seg["end"] - seg["start"],
                    tags=label_names,
                ))

        logger.info(f"  AudioSet total: {len(pairs):,} text-audio pairs")
        return pairs

    def download_audio_batch(
        self, pairs: list[TextAudioPair], max_clips: int = 50000, workers: int = 4
    ) -> list[TextAudioPair]:
        """Download audio for a batch of AudioSet pairs using yt-dlp."""
        audio_dir = self.data_dir / "audio"
        audio_dir.mkdir(exist_ok=True)

        downloaded = []
        items = pairs[:max_clips] if max_clips > 0 else pairs

        for pair in tqdm(items, desc="AudioSet audio", unit="clip"):
            if not pair.audio_url:
                continue

            ytid = pair.audio_url.split("v=")[-1]
            audio_path = audio_dir / f"{ytid}.wav"

            if audio_path.exists():
                pair.audio_path = str(audio_path)
                downloaded.append(pair)
                continue

            try:
                cmd = [
                    "yt-dlp", "--quiet", "--no-warnings",
                    "--extract-audio", "--audio-format", "wav",
                    "--postprocessor-args", f"-ss {pair.duration} -t 10",
                    "-o", str(audio_path),
                    pair.audio_url,
                ]
                result = subprocess.run(cmd, capture_output=True, timeout=60)
                if result.returncode == 0 and audio_path.exists():
                    pair.audio_path = str(audio_path)
                    downloaded.append(pair)
            except Exception:
                continue

        logger.info(f"  AudioSet: {len(downloaded):,} clips downloaded")
        return downloaded


