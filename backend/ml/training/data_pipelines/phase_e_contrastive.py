class MusicCapsDownloader:
    """
    Downloads Google's MusicCaps — 5,521 expert-written music descriptions.
    Each clip is 10 seconds from YouTube with a detailed natural language caption.
    """

    def __init__(self, data_root: Path = DEFAULT_DATA_ROOT):
        self.data_dir = data_root / "musiccaps"
        self.data_dir.mkdir(parents=True, exist_ok=True)

    def download_metadata(self) -> list[dict]:
        """Download MusicCaps CSV from HuggingFace."""
        csv_path = self.data_dir / "musiccaps-public.csv"

        if not csv_path.exists():
            logger.info("  Downloading MusicCaps metadata...")
            resp = requests.get(MUSICCAPS_URL, timeout=60)
            resp.raise_for_status()
            csv_path.write_bytes(resp.content)

        entries = []
        with open(csv_path, newline="") as f:
            reader = csv.DictReader(f)
            for row in reader:
                entries.append(row)

        logger.info(f"  MusicCaps: {len(entries):,} entries")
        return entries

    def download_audio(self, entries: list[dict], max_entries: int = 0) -> list[TextAudioPair]:
        """
        Download audio from YouTube using yt-dlp.
        Each entry has: ytid, start_s, end_s, caption, aspect_list, audioset_positive_labels
        """
        audio_dir = self.data_dir / "audio"
        audio_dir.mkdir(exist_ok=True)
        pairs = []

        items = entries[:max_entries] if max_entries > 0 else entries

        for entry in tqdm(items, desc="MusicCaps", unit="clip"):
            ytid = entry.get("ytid", "")
            start_s = float(entry.get("start_s", 0))
            end_s = float(entry.get("end_s", 10))
            caption = entry.get("caption", "")
            tags = [t.strip() for t in entry.get("aspect_list", "").strip("[]").split(",") if t.strip()]

            if not ytid or not caption:
                continue

            audio_path = audio_dir / f"{ytid}_{int(start_s)}_{int(end_s)}.wav"

            if not audio_path.exists():
                try:
                    url = f"https://www.youtube.com/watch?v={ytid}"
                    cmd = [
                        "yt-dlp", "--quiet", "--no-warnings",
                        "--extract-audio", "--audio-format", "wav",
                        "--postprocessor-args", f"-ss {start_s} -to {end_s}",
                        "-o", str(audio_path),
                        url,
                    ]
                    result = subprocess.run(cmd, capture_output=True, timeout=60)
                    if result.returncode != 0:
                        continue
                except Exception:
                    continue

            if audio_path.exists():
                pairs.append(TextAudioPair(
                    audio_path=str(audio_path),
                    caption=caption,
                    source="musiccaps",
                    duration=end_s - start_s,
                    tags=tags,
                ))

        logger.info(f"  MusicCaps: {len(pairs):,} text-audio pairs downloaded")
        return pairs


