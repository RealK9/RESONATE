class LPMusicCapsDownloader:
    """
    Downloads LP-MusicCaps — 500k+ music clips with LLM-generated captions.
    Built on the Million Song Dataset with pseudo-captions from LLMs.
    """

    def __init__(self, data_root: Path = DEFAULT_DATA_ROOT):
        self.data_dir = data_root / "lp_musiccaps"
        self.data_dir.mkdir(parents=True, exist_ok=True)

    def download_metadata(self) -> list[dict]:
        """Download LP-MusicCaps metadata from HuggingFace."""
        meta_path = self.data_dir / "metadata.jsonl"

        if not meta_path.exists():
            logger.info("  Downloading LP-MusicCaps metadata...")
            # LP-MusicCaps is distributed as parquet files on HuggingFace
            try:
                from datasets import load_dataset
                ds = load_dataset("seungheondoh/LP-MusicCaps-MSD", split="train")
                entries = [dict(row) for row in ds]

                with open(meta_path, "w") as f:
                    for entry in entries:
                        f.write(json.dumps(entry) + "\n")

                logger.info(f"  LP-MusicCaps: {len(entries):,} entries")
                return entries

            except ImportError:
                logger.warning("  'datasets' library not installed — install with: pip install datasets")
                logger.info("  Attempting direct download...")

                # Fallback: download parquet files directly
                for split_idx in range(10):
                    url = f"{LP_MUSICCAPS_URL}/train-{split_idx:05d}-of-00010.parquet"
                    parquet_path = self.data_dir / f"train-{split_idx:05d}.parquet"
                    if not parquet_path.exists():
                        try:
                            resp = requests.get(url, timeout=120)
                            if resp.status_code == 200:
                                parquet_path.write_bytes(resp.content)
                        except Exception as e:
                            logger.warning(f"  Failed to download split {split_idx}: {e}")
                            break

                return []

        entries = []
        with open(meta_path) as f:
            for line in f:
                entries.append(json.loads(line))
        logger.info(f"  LP-MusicCaps: {len(entries):,} entries loaded from cache")
        return entries

    def build_pairs(self, entries: list[dict]) -> list[TextAudioPair]:
        """Build text-audio pairs from LP-MusicCaps metadata.
        Note: Audio must be sourced separately (7digital previews or user-provided).
        """
        pairs = []
        for entry in entries:
            caption = entry.get("caption", "") or entry.get("pseudo_caption", "")
            track_id = entry.get("track_id", "") or entry.get("id", "")

            if not caption:
                continue

            pairs.append(TextAudioPair(
                audio_path="",  # Resolved at training time
                caption=caption,
                source="lp_musiccaps",
                tags=entry.get("tags", []),
            ))

        logger.info(f"  LP-MusicCaps: {len(pairs):,} caption pairs (audio resolved at train time)")
        return pairs


