class FreesoundDownloader:
    """
    Downloads from Freesound API — 500k+ user-uploaded sounds with text tags.
    Requires a Freesound API key (free registration).
    """

    API_BASE = "https://freesound.org/apiv2"

    def __init__(self, api_key: str, data_root: Path = DEFAULT_DATA_ROOT):
        self.api_key = api_key
        self.data_dir = data_root / "freesound"
        self.data_dir.mkdir(parents=True, exist_ok=True)
        self._session = requests.Session()

    def search_sounds(
        self, query: str = "", filter_str: str = "", page_size: int = 150, max_pages: int = 100
    ) -> list[dict]:
        """Search Freesound for sounds matching query/filter."""
        results = []
        page = 1

        while page <= max_pages:
            params = {
                "token": self.api_key,
                "page_size": page_size,
                "page": page,
                "fields": "id,name,tags,description,duration,previews,avg_rating,num_ratings",
            }
            if query:
                params["query"] = query
            if filter_str:
                params["filter"] = filter_str

            try:
                resp = self._session.get(f"{self.API_BASE}/search/text/", params=params, timeout=30)
                if resp.status_code != 200:
                    break
                data = resp.json()
                results.extend(data.get("results", []))

                if not data.get("next"):
                    break
                page += 1
                time.sleep(0.1)
            except Exception as e:
                logger.warning(f"Freesound search error: {e}")
                break

        logger.info(f"  Freesound search '{query}': {len(results):,} results")
        return results

    def download_sounds(self, sounds: list[dict], max_sounds: int = 0) -> list[TextAudioPair]:
        """Download audio previews and build text-audio pairs."""
        audio_dir = self.data_dir / "audio"
        audio_dir.mkdir(exist_ok=True)
        pairs = []

        items = sounds[:max_sounds] if max_sounds > 0 else sounds

        for sound in tqdm(items, desc="Freesound", unit="sound"):
            sound_id = sound.get("id")
            name = sound.get("name", "")
            description = sound.get("description", "")
            tags = sound.get("tags", [])
            duration = sound.get("duration", 0)
            previews = sound.get("previews", {})

            # Build caption from name + description + tags
            caption = description if description else name
            if tags and len(caption) < 20:
                caption = f"{name}: {', '.join(tags[:5])}"

            # Download preview (HQ MP3 or OGG)
            preview_url = previews.get("preview-hq-mp3") or previews.get("preview-hq-ogg")
            if not preview_url:
                continue

            ext = ".mp3" if "mp3" in preview_url else ".ogg"
            audio_path = audio_dir / f"{sound_id}{ext}"

            if not audio_path.exists():
                try:
                    resp = self._session.get(preview_url, timeout=30)
                    if resp.status_code == 200:
                        audio_path.write_bytes(resp.content)
                    time.sleep(0.05)
                except Exception:
                    continue

            if audio_path.exists():
                pairs.append(TextAudioPair(
                    audio_path=str(audio_path),
                    caption=caption,
                    source="freesound",
                    duration=duration,
                    tags=tags,
                ))

        logger.info(f"  Freesound: {len(pairs):,} text-audio pairs")
        return pairs


