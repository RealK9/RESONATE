@dataclass
class TextAudioPair:
    """A single text-audio training pair for contrastive learning."""
    audio_path: str
    caption: str
    source: str  # wavcaps, musiccaps, lp_musiccaps, audioset, freesound
    audio_url: Optional[str] = None
    duration: float = 0.0
    tags: list[str] = field(default_factory=list)


