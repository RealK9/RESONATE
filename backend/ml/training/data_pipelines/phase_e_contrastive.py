class PhaseEPipeline:
    """
    Master pipeline for Phase E contrastive data.
    Orchestrates all downloaders and builds the unified text-audio dataset.
    """

    def __init__(self, data_root: Path = DEFAULT_DATA_ROOT, freesound_api_key: str = ""):
        self.data_root = data_root
        self.data_root.mkdir(parents=True, exist_ok=True)
        self.freesound_api_key = freesound_api_key

        # State DB for tracking downloads
        self.db_path = data_root / "phase_e_state.db"
        self._init_db()

    def _init_db(self):
        conn = sqlite3.connect(str(self.db_path))
        conn.execute("""
            CREATE TABLE IF NOT EXISTS pairs (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                audio_path TEXT,
                caption TEXT,
                source TEXT,
                duration REAL,
                tags TEXT,
                downloaded INTEGER DEFAULT 0
            )
        """)
        conn.execute("CREATE INDEX IF NOT EXISTS idx_source ON pairs(source)")
        conn.commit()
        conn.close()

    def download_all_metadata(self) -> dict[str, int]:
        """Download metadata for all Phase E sources."""
        stats = {}

        # WavCaps
        logger.info("Downloading WavCaps metadata...")
        wc = WavCapsDownloader(self.data_root)
        wc_meta = wc.download_metadata()
        total_wc = sum(len(v) for v in wc_meta.values())
        stats["wavcaps"] = total_wc

        # MusicCaps
        logger.info("Downloading MusicCaps metadata...")
        mc = MusicCapsDownloader(self.data_root)
        mc_entries = mc.download_metadata()
        stats["musiccaps"] = len(mc_entries)

        # LP-MusicCaps
        logger.info("Downloading LP-MusicCaps metadata...")
        lp = LPMusicCapsDownloader(self.data_root)
        lp_entries = lp.download_metadata()
        stats["lp_musiccaps"] = len(lp_entries)

        # AudioSet
        logger.info("Downloading AudioSet metadata and ontology...")
        aset = AudioSetDownloader(self.data_root)
        aset.download_ontology()
        segments = aset.download_segment_csvs()
        total_as = sum(len(v) for v in segments.values())
        stats["audioset"] = total_as

        logger.info(f"\nPhase E metadata summary:")
        for source, count in stats.items():
            logger.info(f"  {source}: {count:,}")
        logger.info(f"  TOTAL: {sum(stats.values()):,}")

        return stats

    def download_audio(self, max_per_source: int = 0) -> dict[str, int]:
        """Download audio for all sources. Expensive — use wisely."""
        stats = {}

        # WavCaps audio (direct download, most accessible)
        logger.info("Downloading WavCaps audio...")
        wc = WavCapsDownloader(self.data_root)
        wc_meta = wc.download_metadata()
        wc_pairs = wc.download_audio(wc_meta, max_per_source=max_per_source)
        stats["wavcaps"] = len(wc_pairs)

        # MusicCaps audio (requires yt-dlp)
        logger.info("Downloading MusicCaps audio (via yt-dlp)...")
        mc = MusicCapsDownloader(self.data_root)
        mc_entries = mc.download_metadata()
        mc_pairs = mc.download_audio(mc_entries, max_entries=max_per_source)
        stats["musiccaps"] = len(mc_pairs)

        # AudioSet audio (requires yt-dlp, very large)
        logger.info("Downloading AudioSet audio (via yt-dlp)...")
        aset = AudioSetDownloader(self.data_root)
        aset.download_ontology()
        segments = aset.download_segment_csvs()
        as_pairs = aset.segments_to_pairs(segments)
        as_downloaded = aset.download_audio_batch(as_pairs, max_clips=max_per_source)
        stats["audioset"] = len(as_downloaded)

        # Freesound (requires API key)
        if self.freesound_api_key:
            logger.info("Downloading Freesound audio...")
            fs = FreesoundDownloader(self.freesound_api_key, self.data_root)
            # Search for music production sounds
            queries = [
                "drum loop", "bass", "synth pad", "vocal chop", "hi hat",
                "808", "snare", "kick drum", "piano chord", "guitar riff",
                "ambient texture", "risers", "impacts", "foley", "orchestra",
            ]
            all_sounds = []
            for q in queries:
                sounds = fs.search_sounds(query=q, max_pages=10)
                all_sounds.extend(sounds)
            fs_pairs = fs.download_sounds(all_sounds, max_sounds=max_per_source)
            stats["freesound"] = len(fs_pairs)

        logger.info(f"\nPhase E audio download summary:")
        for source, count in stats.items():
            logger.info(f"  {source}: {count:,}")
        logger.info(f"  TOTAL: {sum(stats.values()):,}")

        return stats


