def get_all_instruments() -> list[Instrument]:
    """Return every instrument in the registry."""
    return list(_INSTRUMENTS)


def get_instruments_by_category(category: str) -> list[Instrument]:
    """Return instruments matching *category* (case-insensitive)."""
    cat = category.lower()
    return [i for i in _INSTRUMENTS if i.category.lower() == cat]


def get_instrument_by_name(name: str) -> Optional[Instrument]:
    """Fuzzy lookup by canonical name or alias (case-insensitive, ignores
    hyphens/underscores/spaces)."""
    def _normalize(s: str) -> str:
        return re.sub(r"[\s\-_()]", "", s.lower())

    target = _normalize(name)
    for inst in _INSTRUMENTS:
        candidates = [inst.name] + list(inst.aliases)
        for c in candidates:
            if _normalize(c) == target:
                return inst
    # Substring fallback
    for inst in _INSTRUMENTS:
        candidates = [inst.name] + list(inst.aliases)
        for c in candidates:
            if target in _normalize(c) or _normalize(c) in target:
                return inst
    return None


def get_instrument_labels() -> list[str]:
    """Return a sorted list of all canonical instrument names for the classification head."""
    return sorted(i.name for i in _INSTRUMENTS)


def get_category_hierarchy() -> dict[str, dict[str, list[str]]]:
    """Return {category: {subcategory: [instrument_name, ...]}}."""
    hier: dict[str, dict[str, list[str]]] = {}
    for inst in _INSTRUMENTS:
        hier.setdefault(inst.category, {}).setdefault(inst.subcategory, []).append(inst.name)
    return hier


def generate_clap_descriptions(
    descriptions_per_instrument: int = 5,
    seed: Optional[int] = 42,
) -> list[tuple[str, str]]:
    """Generate (text_description, instrument_label) pairs for CLAP alignment.

    Each instrument receives *descriptions_per_instrument* natural-language
    descriptions that vary playing style, register, genre context and effects.

    Parameters
    ----------
    descriptions_per_instrument : int
        Number of textual descriptions to generate per instrument.
    seed : int | None
        Random seed for reproducibility.  ``None`` for non-deterministic output.

    Returns
    -------
    list[tuple[str, str]]
        Each tuple is (description_text, instrument_name).
    """
    rng = random.Random(seed)
    results: list[tuple[str, str]] = []

    for inst in _INSTRUMENTS:
        templates = _CLAP_TEMPLATES.get(inst.category, _CLAP_TEMPLATES[CAT_ELECTRONIC])
        for _ in range(descriptions_per_instrument):
            tmpl = rng.choice(templates)
            filled = tmpl
            for key, options in _FILL_INS.items():
                placeholder = "{" + key + "}"
                if placeholder in filled:
                    # Prefer genre_associations from instrument when filling {genre}
                    if key == "genre" and inst.genre_associations:
                        choice = rng.choice(inst.genre_associations)
                    else:
                        choice = rng.choice(options)
                    filled = filled.replace(placeholder, choice, 1)
            # Replace {name} last
            filled = filled.replace("{name}", inst.name)
            results.append((filled, inst.name))

    return results


